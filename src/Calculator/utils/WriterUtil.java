package Calculator.utils;

/**
 * Операционный класс для вывода текста в консоль
 */

public class WriterUtil {
    public static void displayDouble(double result) {
        System.out.println(result);
    }

    public static void displayInt(int result) {
        System.out.println(result);
    }

    public static void displayString(String result) {
        System.out.println(result);
    }
}
